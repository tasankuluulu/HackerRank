package com.hackerrank.problems;

public class DeclaringVariables {
	public static void main(String[] args) {
		int a = 63;
		float b = 45.0f;
		double c = 67.675586;
		System.out.println("The value of first variable is " + a);
		System.out.println("The value of second variable is " + b);
		System.out.println("The value of third variable is " + c);		
	}
}
